# Character dimensions
CHAR_W = 6  # 5 pixels + 1 spacing
CHAR_H = 8  # 7 pixels + 1 spacing


def draw_text(surface, x, y, text, color=(255, 255, 255), shadow=None):
    """Draw pixel-perfect bitmap text. Optional shadow color for depth."""
    if shadow:
        draw_text(surface, x + 1, y + 1, text, color=shadow)
    cx = x
    for ch in text:
        glyph = GLYPHS.get(ch.upper() if ch.upper() in GLYPHS else ch, GLYPHS.get(" "))
        if glyph is None:
            glyph = GLYPHS[" "]
        for row_idx, row_bits in enumerate(glyph):
            for col in range(5):
                if row_bits & (0b10000 >> col):
                    px = cx + col
                    py = y + row_idx
                    if 0 <= px < surface.get_width() and 0 <= py < surface.get_height():
                        surface.set_at((px, py), color)
        cx += CHAR_W


def text_width(text):
    """Return pixel width of text string."""
    return len(text) * CHAR_W


def draw_text_centered(surface, cx, y, text, color=(255, 255, 255), shadow=None):
    """Draw text centered horizontally on cx."""
    w = text_width(text)
    draw_text(surface, cx - w // 2, y, text, color, shadow)
